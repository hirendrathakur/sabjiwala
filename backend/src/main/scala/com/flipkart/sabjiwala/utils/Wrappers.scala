package com.flipkart.sabjiwala.utils

import scala.util.{Failure, Success, Try}

object Wrappers {

  def Try_[T](f: => T) : Try[T] = {
    try {
      Success(f)
    }
    catch {
      case e: Throwable =>
        println(s"e: ${e.getMessage}",e)
        Failure(e)
    }
  }

}
